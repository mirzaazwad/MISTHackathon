// Side navigation
function w3_openL(){
  var x = document.getElementById("Login");
  x.style.width = "100%";
  x.style.fontSize = "40px";
  x.style.paddingTop = "10%";
  x.style.display = "block";
}
function w3_closeL(){
  document.getElementById("Login").style.display = "none";
}
